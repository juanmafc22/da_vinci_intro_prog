// Realiza un programa que muestre todos los números enteros del
// 1 al 5, y luego los mismos números, pero en orden inverso.
public class EjerRepaso01 {

    public static void main(String[] args) {

        System.out.println("Los numeros del 1 al 5");
        for (int i=1; i<=5; i++) {
            System.out.println(i);
        }
        System.out.println("Los numeros del 5 al 1");
        for (int j=5; j>=1; j--) {
            System.out.println(j);
        }

    }
    
}
